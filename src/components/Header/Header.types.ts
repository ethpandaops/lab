export interface HeaderProps {
  /**
   * Whether to show the network selector in the header
   * @default false
   */
  showNetworkSelector?: boolean;
  /**
   * Whether to show the network summary in the header
   * @default false
   */
  showNetworkSummary?: boolean;
  /**
   * Whether to show breadcrumbs in the NavBar
   * @default true
   */
  showBreadcrumbs?: boolean;
  /**
   * Whether to show navigation links in the NavBar
   * @default true
   */
  showNavLinks?: boolean;
}
